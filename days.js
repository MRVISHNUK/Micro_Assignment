var days=parseInt(prompt("Enter a numbers"));
switch(days){
    case 1: console.log("Sun");
    break;
    case 2: console.log("Mon");
    break; 
    case 3: console.log("Tus");
    break;
    case 4: console.log("Wen");
    break;
    case 5: console.log("Thru");
    break;
    case 6: console.log("Fri");
    break;
    case 7: console.log("Sat");
    break;
    default : console.log("Invalid Choice");
    break;


}










